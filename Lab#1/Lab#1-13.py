#Python - String Concatenation

'''
Конкатенация строк

Чтобы объединить или объединить две строки,
вы можете использовать оператор +.

Объединить переменную "a" с переменной "b" в переменную c:
'''
a = "Hello"
b = "World"
c = a + b
print(c)

a = "Hello"
b = "World"
c = a + " " + b
print(c)